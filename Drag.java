import java.util.Date;

public class Drag {

	private String name;
	private Date dataWprowadzenia;
	private Float weight;
	private String company;
	private String numerSerii;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getDataWprowadzenia() {
		return dataWprowadzenia;
	}

	public void setDataWprowadzenia(Date dataWprowadzenia) {
		this.dataWprowadzenia = dataWprowadzenia;
	}

	public Float getWeight() {
		return weight;
	}

	public void setWeight(Float weight) {
		this.weight = weight;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getNumerSerii() {
		return numerSerii;
	}

	public void setNumerSerii(String numerSerii) {
		this.numerSerii = numerSerii;
	}

}